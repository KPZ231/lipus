      </div>
      
      <div class="rules-intro">
        <p>Łowisko wędkarskie "Lipuś" jest to akwen o łącznej powierzchni 2 ha z czego lustro wody to 1.8 ha. Głębokość wynosi od 1.5 m do 6 m w najgłębszych dno jest bardzo zróżnicowane występują liczne wypłycenia i dziury co z pewnością spodoba się wędkarzom. Zbiornik jest bogaty w rybostan, znajduje się tutaj wiele dorodnych okazów, o których krążą tu legendy.</p>
        
        <p>Stawy będą regularnie zarybiane co zobaczyć będzie można na filmikach, które będziemy dodawać. Na obiekcie będzie prowadzona całoroczna sprzedaż ryb żywych, a także sklepik wędkarski oraz w okresie letnim prowadzona będzie sprzedaż piwa o czym poinformujemy na Facebooku.</p>
        
        <p>Nasze łowisko jest przyjazne rodzinom z dziećmi, dla dzieci jest dostępna piaskownica. Istnieje możliwość grillowania. Dla naszych gości tuż przy łowisku znajduje się duży, bezpłatny parking.</p>
        
        <p class="rules-note"><i class="fas fa-info-circle"></i> Opłata za wędkowanie jest od osoby wędkującej. Szczegółowy cennik dostępny jest na <a href="/#prices">stronie głównej</a>.</p>
        <p class="rules-warning-small"><i class="fas fa-ban"></i> Zakaz spiningowania</p>
