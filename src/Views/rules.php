      <div class="rules-content">
        <h3>REGULAMIN ŁOWISKA</h3>
        
        <ol class="rules-list">
          <li>Łowiskiem zarządza gospodarz, u którego należy obowiązkowo zgłosić każde wejście i wyjście.</li>
          
          <li>Gospodarz łowiska ma prawo do:
            <ul>
              <li>żądania okazania dokumentów tożsamości od osób obecnych na łowisku,</li>
              <li>kontroli pojazdów i bagaży w przypadku podejrzenia, iż złowione ryby nie zostały zgłoszone do zważenia,</li>
              <li>wypraszania z terenu łowiska w przypadku kradzieży, głośnego lub wulgarnego zachowania, śmiecenia czy dewastacji.</li>
            </ul>
          </li>
          
          <li>Opłatę za łowienie należy uiścić obowiązkowo przed wejściem na łowisko wg ustalonego cennika.</li>
          
          <li>Łowienie ryb jest dozwolone tylko w miejscach do tego przeznaczonych.</li>
          
          <li>Zajmowanie stanowiska do wędkowania i wędkowanie powinno odbywać się w taki sposób, aby nie przeszkadzać osobom już wędkującym.</li>
          
          <li>Ryby złowione i przeznaczone do zakupu należy przechowywać w siatce wędkarskiej. Należy je okazać obsłudze przed zakończeniem połowu w celu zważenia i zapłaty wg obowiązującego cennika.</li>
          
          <li>Ryby, których nie kupuje się należy natychmiast wypuścić do wody.</li>
          
          <li>Ograniczmy nęcenie ryb do 1 kg (zakaz nęcenia suchym ziarnem).</li>
          
          <li>Dzieci oraz osoby niepełnoletnie przebywają na terenie łowiska wyłącznie pod opieką i na odpowiedzialność osób dorosłych.</li>
          
          <li>Właściciel łowiska nie ponosi odpowiedzialności za wypadki losowe oraz rzeczy pozostawione na łowisku.</li>
          
          <li>Prosi się o zachowanie szczególnej ostrożności wokół linii brzegowej stawu, jak również na pomostach.</li>
          
          <li>Prosimy utrzymywać porządek, śmieci wyrzucamy do stojących koszy na terenie łowiska, osoby palące obowiązane są do wyrzucania ugaszonych niedopałków papierosów do koszy. <strong>ZAKAZ WYRZUCANIA ŚMIECI DO WODY!</strong></li>
          
          <li>Osoby wkraczające na teren łowiska w miejscu innym niż dozwolone oraz osoby nie uiszczające opłaty za wstęp i za złowione ryby, uważane są za kłusowników. Względem nich będą stosowane odpowiednie konsekwencje karne.</li>
          
          <li>Zakaz przerzucania rzeczy przez płot.</li>
          
          <li>Zakaz kąpieli.</li>
          
          <li>Złowiony pstrąg powinien natychmiast wrócić do wody, lub przy jakimkolwiek uszkodzeniu zdany gospodarzowi łowiska (nie dotyczy zakupu).</li>
          
          <li>Podczas złowienia suma wędkujący ma zakaz wpuszczenia go do wody i zobowiązany jest powiadomić gospodarza oraz oddać go na magazyn. Za złowionego suma od 60 cm wędkujący otrzymuje jednodniową wejściówkę na łowisko.</li>
          
          <li>Obowiązuje mata lub kołyska, obowiązuje delikatne obchodzenie się z rybami.</li>
          
          <li>Obowiązkowe używanie haczyków bezzadziorowych lub z mikrozadziorem.</li>
          
          <li>Na terenie łowiska obowiązuje zakaz spożywania alkoholu nie zakupionego na terenie łowiska.</li>
          
          <li>Wchodząc na teren łowiska akceptujesz regulamin.</li>
        </ol>
        
        <div class="rules-warning">
          <p><i class="fas fa-exclamation-triangle"></i> Złamanie powyższych zasad oraz jakiekolwiek zachowanie niezgodne z ogólnie przyjętymi normami, powoduje usunięcie z łowiska bez zwrotu kosztów, a także odmówienie wstępu w przyszłości.</p>
